class ImageListBalanced(Dataset):
    def __init__(self, image_list, labels=None, transform=None, target_transform=None, class_num=None, mode='RGB'):
        imgs = make_dataset(image_list, labels)
        if len(imgs) == 0:
            raise(RuntimeError("Found 0 images in subfolders of: " + root + "\n"
                               "Supported image extensions are: " + ",".join(IMG_EXTENSIONS)))

        self.imgs = imgs
        self.transform = transform
        self.target_transform = target_transform
        if mode == 'RGB':
            self.loader = rgb_loader
        elif mode == 'L':
            self.loader = l_loader
        self.class_dict = self._get_class_dict()

        assert class_num != None, 'Have not passed arguments needed.'
        self.cls_num = class_num

    def __getitem__(self, index):
        """Balanced Sampling
        step1. Select one class from uniform distribution.
        step2. Randomly select one sample from the selected class.
        """
        sample_class = random.randint(0, self.cls_num - 1)
        sample_indexes = self.class_dict[sample_class]
        index = random.choice(sample_indexes)

        path, target = self.imgs[index]
        img = self.loader(path)
        if self.transform is not None:
            img = self.transform(img)
        if self.target_transform is not None:
            target = self.target_transform(target)

        return img, target

    def __len__(self):
        return len(self.imgs)


    def get_annotations(self):
        annos = []
        for (img_path, lb) in self.imgs:
            annos.append({'category_id': int(lb)})
        return annos

    def _get_class_dict(self):
        class_dict = dict()
        for i, anno in enumerate(self.get_annotations()):
            cat_id = anno["category_id"]
            if not cat_id in class_dict:
                class_dict[cat_id] = []
            class_dict[cat_id].append(i)
        return class_dict



